app.post("/render-clips", async (req, res) => {
  const body = (req.body ?? {}) as Partial<ClipManifest>;
  let cleanupVideo: (() => Promise<void>) | null = null;

  try {
    if (!body || typeof body !== "object") {
      throw new Error("Body must be a JSON object.");
    }

    if (!Array.isArray(body.clips) || body.clips.length === 0) {
      throw new Error("`clips` must be a non-empty array.");
    }

    if (typeof body.video !== "string" && typeof body.videoKey !== "string") {
      throw new Error("Either `video` (URL or path) or `videoKey` (S3 key) must be provided.");
    }

    // Optionally load a transcript JSON from disk that contains a `segments`
    // array with `{ start, end, text }` entries. We'll slice this per-clip to
    // derive relative caption cues when a clip doesn't specify captions
    // explicitly.
    type TranscriptSegment = { start: number; end: number; text: string };
    let transcriptSegments: TranscriptSegment[] | null = null;

    if (typeof body.transcriptPath === "string" && body.transcriptPath.trim() !== "") {
      const transcriptPath = body.transcriptPath.trim();
      try {
        const raw = await readFile(transcriptPath, { encoding: "utf8" });
        const parsed = JSON.parse(raw) as { segments?: unknown };
        if (!Array.isArray(parsed.segments)) {
          throw new Error("Transcript JSON is missing a segments array.");
        }

        transcriptSegments = (parsed.segments as unknown[])
          .map((segment) => {
            if (typeof segment !== "object" || segment === null) return null;
            const candidate = segment as { start?: unknown; end?: unknown; text?: unknown };
            const start = Number(candidate.start);
            const end = Number(candidate.end);
            const text = typeof candidate.text === "string" ? candidate.text.trim() : "";
            if (!Number.isFinite(start) || !Number.isFinite(end) || !text) return null;
            return { start, end, text } satisfies TranscriptSegment;
          })
          .filter((segment): segment is TranscriptSegment => segment !== null);

        console.log("Loaded transcript for render-clips", {
          transcriptPath,
          segmentCount: transcriptSegments.length,
        });
      } catch (error) {
        console.error("Failed to load transcriptPath for render-clips", {
          transcriptPath: body.transcriptPath,
          error,
        });
        throw new Error("Unable to read transcriptPath JSON. See server logs for details.");
      }
    }

    const localizedVideo = await localizeVideoSource({
      video: typeof body.video === "string" ? body.video : undefined,
      videoKey: typeof body.videoKey === "string" ? body.videoKey : undefined,
    });
    cleanupVideo = localizedVideo.cleanup;
    const videoUrl = localizedVideo.url;
    const brandId = typeof body.brandId === "string" ? body.brandId : undefined;
    const manifestAuthorHandle = typeof body.authorHandle === "string" ? body.authorHandle : undefined;
    const manifestAvatarUrl = typeof body.avatarUrl === "string" ? body.avatarUrl : undefined;

    const serveUrl = await ensureBundle();

    const results: Array<{
      id: string;
      bucket: string | null;
      key: string;
      endpoint?: string | undefined;
      location: string;
      url?: string | undefined;
      proxyUrl?: string | undefined;
    }> = [];

    for (const clip of body.clips) {
      if (!clip || typeof clip !== "object") {
        throw new Error("Every clip must be an object.");
      }

      const { id, start, end, title } = clip;
      if (!id || typeof id !== "string") {
        throw new Error("Each clip must have an `id` string.");
      }
      if (typeof start !== "number" || typeof end !== "number" || !Number.isFinite(start) || !Number.isFinite(end) || end <= start) {
        throw new Error(`Clip ${id} must have numeric start/end seconds with end > start.`);
      }
      if (!title || typeof title !== "string") {
        throw new Error(`Clip ${id} must have a non-empty title.`);
      }

      let captions = Array.isArray(clip.captions)
        ? clip.captions
            .filter((cue) =>
              cue && typeof cue.start === "number" && typeof cue.end === "number" && typeof cue.text === "string",
            )
            .map((cue) => ({
              start: cue.start,
              end: cue.end,
              text: cue.text,
            }))
        : [];

      // If no explicit captions are provided but we have a transcript
      // loaded, derive clip-relative caption cues by slicing the global
      // transcript between the clip's absolute start/end times.
      if (captions.length === 0 && transcriptSegments && transcriptSegments.length > 0) {
        const clipDuration = end - start;
        const MIN_AUTO_CAPTION_DURATION = 0.5; // seconds

        const cues = transcriptSegments
          .map((segment) => {
            const segStart = segment.start;
            const segEnd = segment.end;
            if (!Number.isFinite(segStart) || !Number.isFinite(segEnd)) return null;
            if (segEnd <= start || segStart >= end) return null;

            const relativeStart = Math.max(0, segStart - start);
            const relativeEnd = Math.min(clipDuration, segEnd - start);
            if (relativeEnd <= relativeStart) return null;

            return {
              start: Number(relativeStart.toFixed(2)),
              end: Number(relativeEnd.toFixed(2)),
              text: segment.text,
            };
          })
          .filter((cue): cue is { start: number; end: number; text: string } => cue !== null);

        const filtered = cues.filter((cue) => cue.end - cue.start >= MIN_AUTO_CAPTION_DURATION);
        captions = filtered;
        console.log("Derived captions from transcript for clip", {
          id,
          clipDuration,
          captionCount: captions.length,
        });
      }

      const props = {
        videoUrl,
        title,
        clipId: id,
        brandId,
        authorHandle: typeof clip.authorHandle === "string" ? clip.authorHandle : manifestAuthorHandle,
        avatarUrl: typeof clip.avatarUrl === "string" ? clip.avatarUrl : manifestAvatarUrl,
        startSeconds: start,
        endSeconds: end,
        captions,
      } satisfies Record<string, unknown>;

      const outputKey = composeKey(`${id}-${Date.now()}`, REMOTION_SHORT_COMPOSITION_ID);
      const tmpFile = path.join(tmpdir(), `remotion-clip-${id}-${randomUUID()}.mp4`);

      console.log("Short clip render requested", {
        composition: REMOTION_SHORT_COMPOSITION_ID,
        clipId: id,
        title,
        start,
        end,
        outputKey,
      });

      const videoConfig = await selectComposition({
        serveUrl,
        id: REMOTION_SHORT_COMPOSITION_ID,
        inputProps: props,
      });

      await renderMedia({
        serveUrl,
        composition: videoConfig,
        outputLocation: tmpFile,
        inputProps: props,
        codec: "h264",
        enforceAudioTrack: true,
        jpegQuality: 90,
        logLevel: "verbose",
        timeoutInMilliseconds: 120000,
        concurrency: 1,
        onBrowserLog: ({ text, type }) => {
          console.log("ShortClip browser log", { type, text });
        },
      });

      const upload = await uploadToS3(tmpFile, outputKey);
      console.log("Short clip upload complete", {
        clipId: id,
        bucket: upload.bucket ?? bucketName,
        key: upload.key,
        location: upload.location,
      });

      results.push({
        id,
        bucket: upload.bucket ?? bucketName ?? null,
        key: upload.key,
        endpoint,
        location: upload.location,
        url: upload.url,
        proxyUrl: upload.proxyUrl,
      });
    }

    res.status(201).json({
      video: body.video,
      clips: results,
    });
  } catch (error) {
    const detail = (error as Error)?.message ?? "Unknown clip render error";
    console.error("render-clips failed", error);
    res.status(400).json({
      error: "Invalid clip render input",
      detail,
    });
  } finally {
    if (cleanupVideo) {
      try {
        await cleanupVideo();
      } catch (error) {
        console.error("Video cleanup after render-clips failed", error);
      }
    }
  }
});

async function renderScene(body: unknown): Promise<RenderJobResult> {
  const requestBody = getRawProps(body);
  const composition = getRenderComposition(requestBody);
  const requestedKey =
    typeof requestBody.outputKey === "string" ? requestBody.outputKey.trim() : undefined;
  const outputKey = composeKey(requestedKey, composition);
  const tmpFile = path.join(tmpdir(), `remotion-${randomUUID()}.mp4`);
  let cleanupLocalizedSegments: (() => Promise<void>) | null = null;

  try {
    const rawProps = getRawProps(requestBody.props);
    const normalizedSegments = validateRenderSegments(composition, rawProps);
    const { localizedSegments, localFilePaths, cleanup } = await localizeSegments(normalizedSegments);
    cleanupLocalizedSegments = cleanup;
    const measuredDurationsMs = await Promise.all(
      localFilePaths.map(async (filePath, index) => {
        try {
          return await probeAudioDurationMs(filePath);
        } catch (error) {
          console.warn("Unable to probe localized segment duration", {
            index,
            filePath,
            error,
          });
          return normalizedSegments[index]?.durationMs ?? localizedSegments[index]?.durationMs ?? 0;
        }
      }),
    );
    const segmentsWithResolvedDurations = localizedSegments.map((segment, index) => {
      const providedDurationMs = normalizedSegments[index]?.durationMs ?? segment.durationMs;
      const measuredDurationMs = measuredDurationsMs[index];
      const resolvedDurationMs = Math.max(
        segment.durationMs,
        providedDurationMs,
        Number.isFinite(measuredDurationMs)
          ? measuredDurationMs + AUDIO_DURATION_TAIL_PADDING_MS
          : 0,
      );

      return {
        ...segment,
        durationMs: resolvedDurationMs,
      };
    });
    const waveform = await buildWaveform(
      localFilePaths,
      segmentsWithResolvedDurations.map((segment) => segment.durationMs),
    );
    const props: SceneProps & Record<string, unknown> = {
      ...rawProps,
      segments: segmentsWithResolvedDurations,
      waveform,
    };
    console.log("Render requested", {
      composition,
      propsPreview: {
        title: props?.title,
        segments: Array.isArray(props?.segments) ? props.segments.length : undefined,
        waveformBars: props.waveform?.length,
        requestedDurationMs: normalizedSegments.reduce(
          (total, segment) => total + segment.durationMs,
          0,
        ),
        resolvedDurationMs: segmentsWithResolvedDurations.reduce(
          (total, segment) => total + segment.durationMs,
          0,
        ),
      },
      outputKey,
    });
    const serveUrl = await ensureBundle();
    const videoConfig = await selectComposition({
      serveUrl,
      id: composition,
      inputProps: props,
      onBrowserDownload: ({ chromeMode }) => {
        console.log("Ensuring Chromium is available", { chromeMode });
        return {
          version: null,
          onProgress: () => undefined,
        };
      },
    });
    console.log("renderMedia start", { composition, outputLocation: tmpFile });
    await renderMedia({
      serveUrl,
      composition: videoConfig,
      outputLocation: tmpFile,
      inputProps: props,
      codec: "h264",
      enforceAudioTrack: true,
      jpegQuality: 90,
      logLevel: "error",
      concurrency: 1,
      timeoutInMilliseconds: 120000,
      onDownload: (src) => {
        console.log("Renderer downloading media", { src });
        return ({ percent, downloaded, totalSize }) => {
          if (percent === 1 || (totalSize !== null && downloaded >= totalSize)) {
            console.log("Renderer media download complete", { src, downloaded, totalSize });
          }
        };
      },
      onBrowserLog: ({ text, type }) => {
        if (type === "warning" || type === "error") {
          console.warn("Browser log", { type, text });
        }
      },
    });
    console.log("renderMedia complete", { composition, outputLocation: tmpFile });

    const upload = await uploadToS3(tmpFile, outputKey);
    console.log("uploadToS3 complete", {
      bucket: upload.bucket ?? bucketName,
      key: upload.key,
      location: upload.location,
      url: upload.url,
      proxyUrl: upload.proxyUrl,
    });

    const result = {
      bucket: upload.bucket ?? bucketName ?? null,
      key: upload.key,
      endpoint,
      location: upload.location,
      url: upload.url,
      proxyUrl: upload.proxyUrl,
    } satisfies RenderJobResult;

    console.log("Render result ready", result);
    return result;
  } finally {
    try {
      await cleanupRenderArtifacts({
        cleanupLocalizedSegments,
        tmpFile,
      });
      console.log("Render cleanup complete", { outputKey });
    } catch (error) {
      console.error("Render cleanup failed", error);
    }
  }
}

