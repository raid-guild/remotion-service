function buildObjectKey(value: string): string {
  const sanitized = value.replace(/^\/+/, "").replace(/\/+$/, "");
  if (!prefix) return sanitized;
  return `${prefix}/${sanitized}`;
}

function ensureMp4(fileName: string): string {
  if (fileName.toLowerCase().endsWith(".mp4")) return fileName;
  return `${fileName}.mp4`;
}

function composeKey(suggested?: string, compositionId?: string): string {
  const base =
    (suggested && suggested.trim()) ||
    `${(compositionId ?? "remotion").replace(/[^a-z0-9-_]/gi, "-")}-${Date.now()}`;
  return buildObjectKey(ensureMp4(base));
}

function encodeKeyPath(key: string): string {
  return key
    .split("/")
    .filter((part) => part.length > 0)
    .map((part) => encodeURIComponent(part))
    .join("/");
}

function buildPublicOutputUrl(key: string): string | undefined {
  if (!publicOutputBaseUrl) return undefined;
  return `${publicOutputBaseUrl}/${encodeKeyPath(key)}`;
}

function buildProxyOutputUrl(key: string): string {
  const proxyPath = `/render-outputs/${encodeKeyPath(key)}`;
  return servicePublicBaseUrl ? `${servicePublicBaseUrl}${proxyPath}` : proxyPath;
}

async function uploadToS3(localPath: string, key: string): Promise<{
  bucket: string | null;
  key: string;
  location: string;
  url?: string | undefined;
  proxyUrl: string;
}> {
  if (!bucketName) {
    if (!localOutputDir) {
      throw new Error(
        "Local output requested but REMOTION_LOCAL_OUTPUT_DIR is not set. This should not happen.",
      );
    }

    const baseDir = localOutputDir.replace(/\/+$/, "");
    const destination = path.join(baseDir, key);
    await mkdir(path.dirname(destination), { recursive: true });
    await copyFile(localPath, destination);

    return {
      bucket: null,
      key,
      location: destination,
      url: undefined,
      proxyUrl: buildProxyOutputUrl(key),
    };
  }

  const stream = createReadStream(localPath);
  await s3Client!.send(
    new PutObjectCommand({
      Bucket: bucketName,
      Key: key,
      Body: stream,
      ContentType: "video/mp4",
    }),
  );

  return {
    bucket: bucketName,
    key,
    location: key,
    url: buildPublicOutputUrl(key),
    proxyUrl: buildProxyOutputUrl(key),
  };
}

